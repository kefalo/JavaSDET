package dataTypes;

public class VariablesAndFirstJavaProgram {

	public static void main(String[] args) {
		//Variables and data types in Java
		
		
		int a = 10; // integer a whole number
		
		String s = "stefan"; //sentence, word, text
		
		char c = 's'; // only one character
		
		double d = 10.10010101; // whole number with up to 16 decimal places
		
		boolean b = true;
		
		System.out.println(a);
		System.out.println(s);
		System.out.println(c);
		System.out.println(d);
		System.out.println(b);

	}

}
