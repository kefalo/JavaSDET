package loops;

public class ForLoop {

	public static void main(String[] args) {
		//for loop
		
		//initialize value-->condition-->inc/dec
		
		/*
		 for(initialize; condition; inc/dec) {
		 	//code
		 }
		 */
		
		for(int i = 10; i>=1; i--) {
			System.out.println(i);
		}
		System.out.println("Program exited");

	}

}
